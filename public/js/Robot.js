
function show_message01() {
	var objDiv = document.getElementById("Robot_MessageBox1");
	objDiv.style.visibility = "visible"; //改变属性可见
}
//第二个函数默认就已经执行了<button在那个Div里面>
function hidden_message01() {
	var objDiv = document.getElementById("Robot_MessageBox1");
	objDiv.style.visibility = "hidden"; //改变属性可见
}

